<h2 class="mb-5">Chronik aus dem Jahr 2021</h2>

<p>Bedingt durch die 2. und 3. Coronawelle, die sich bis Anfang Juni 2021 hinzog, waren bis dahin keine Proben möglich.&nbsp;</p>
<p>Am 21. Juni wollten wir dann unsere 1. Probe am Wasserturm starten.</p>
<p>Leider machte uns ein schweres Gewitter dies nicht möglich.&nbsp;</p>
<p>Der Probenbetrieb lief dann bis zum Beginn der Sommerferien.</p>
<p>Zum Saisonabschluss am 26. Juli trafen&nbsp; wir uns&nbsp; in der Altstadtschänke zum gemütlichen Beisammensein.&nbsp;</p>
<p>Am 13. September starteten die Proben wieder nach den Sommerferien.&nbsp;</p>
<p>Am 9. Oktober fand die Versammlung des Landesverband der Spielmanns- und Fanfarenzüge in Baden-Württemberg 1957 e.V. in Rottweil statt.</p>
<p>Leider übernahm dann Corona &nbsp;wieder die Oberhand und so musste der für den 20. November angesagte Stabführerlehrgang in Rottweil abgesagt werden.</p>
<p>Durch das hohe Infektionsgeschehen und die neuen Corona Verordnungen&nbsp; konnte keine Probe mehr stattfinden.</p>