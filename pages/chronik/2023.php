<h2 class="mb-5">Chronik aus dem Jahr 2023</h2>

<p>Die Rottweiler Sportlerehrung fand am <strong>17.März 2023</strong> statt. Jessica Vogt wurde geehrt mit Silber. Sie belegte den 10. Platz von 27 Startern  von 9 Vereinen aus 3 Nationen bei der Weltmeisterschaft  im Fahnenhochwurf  in Konstanz.</p>
<p>Am <strong>25. März 2023</strong> fand die Mitgliederversammlung des Landesverbands der Spielmanns-und Fanfarenzüge in Reute statt.</p>
<p>Am <b>24. April 2023</b> feierte unser ältestes Mitglied, Frau  Christa Hauptmann, ihren 80. Geburtstag.  Der Stellvertretende Vorsitzende Hans-Peter Bucher  vom Fanfarenzug  Rottweil  gratulierte der Jubilarin und überreichte einen Blumenstrauß.</p>
<p>Die Mitgliederversammlung des Fanfarenzug Rottweil 1978 e. V. wurde in der Pension Haas Hotel am Turm  am Samstag <b>17. Juni 2023</b> abgehalten. Bei den Wahlen wurden sämtliche Amtsinhaber in ihren Ämtern bestätigt. Gewählt wurde der Stv. Vorsitzende Hans-Peter Bucher, Schriftführer Thomas Wiedemann sowie der Pressewart Thomas Wiedemann und als 2. Beirat Jessica Vogt.</p>
